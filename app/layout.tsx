import type { Metadata } from "next";
import { Inter } from "next/font/google";
import "./globals.css";
import { Toaster } from "react-hot-toast";

import React from "react";
import { ThemeProvider } from "@/components/providers/theme/theme-provider";
import QueryProvider from "@/components/providers/context/QueryProvider";
import { SidebarProvider } from "@/components/providers/context/SidebarContext";
import { AuthProvider } from "@/components/providers/context/auth-context";
import ClientOnly from "./ClientOnly";

// Configure inter font with all needed weights and subsets
const poppins = Inter({
  subsets: ["latin"],
  weight: ["100", "200", "300", "400", "500", "600", "700"],
  variable: "--font-poppins", // Optional: for using CSS variables
});

export const metadata: Metadata = {
  title: "Servicyee",
  description: "Generated by Servicyee",
  // Optional: Add more metadata for better SEO
  metadataBase: new URL(
    process.env.NEXT_PUBLIC_SITE_URL || "http://localhost:3000"
  ),
};

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  return (
    <html lang="en" suppressHydrationWarning className={poppins.className}>
      <body className={`antialiased bg-white dark:bg-gray-900 text-gray-900 dark:text-gray-100 transition-colors duration-300`}>
        <ThemeProvider
          attribute="class"
          defaultTheme="system"
          enableSystem
          disableTransitionOnChange
        >
          <QueryProvider>
            <SidebarProvider>
              <AuthProvider>
                <ClientOnly>
                  {children}
                </ClientOnly>
              </AuthProvider>
              <Toaster position="top-right" reverseOrder={false} />
            </SidebarProvider>
          </QueryProvider>
        </ThemeProvider>
      </body>
    </html>
  );
}

